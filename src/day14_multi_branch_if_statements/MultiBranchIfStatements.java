package day14_multi_branch_if_statements;

public class MultiBranchIfStatements {
    public static void main(String[] args) {
        int day = 1;
        if (day == 1) {
            System.out.println("Monday");
        } else if (day == 2) {
            System.out.println("Tuesday");
        } else if (day == 3) {
            System.out.println("Wednesday");
        }
    }
}
